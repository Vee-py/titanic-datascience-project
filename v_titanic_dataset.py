from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, confusion_matrix

df["embarked"].fillna("S",inplace=True)

df["deck"].fillna("S",inplace=True)



df["sex"]=df["sex"].map({"male":0,"female":1})
df["embarked"]=df["embarked"].map({"S":0,"C":1,"Q":2})
df["alone"]=df["alone"].map({True:1,False:0})

df["adult_male"] = df["adult_male"].map({True: 1, False: 0})
df["alive"] = df["alive"].map({"yes": 1, "no": 0})

categorical_cols = ["class", "who", "deck", "embark_town"]
# Filter to include only columns that still exist in df
existing_categorical_cols = [col for col in categorical_cols if col in df.columns]
df = pd.get_dummies(df, columns=existing_categorical_cols, drop_first=True)

# Drop 'survived' and 'alive' as 'alive' is a direct proxy for 'survived'
x = df.drop(["survived", "alive"], axis=1)
y = df["survived"]

# Fill NaNs in 'adult_male' column within x DataFrame
x['adult_male'].fillna(0, inplace=True) # Assuming 0 for False/unknown adult male

print("NaNs in feature DataFrame x after preprocessing:")
print(x.isnull().sum())

x_train,x_test,y_train,y_test=train_test_split(
    x,y,test_size=0.2,random_state=42
)

model=LogisticRegression(max_iter=1000)
model.fit(x_train,y_train)

y_pred=model.predict(x_test)

print("Accuracy:",accuracy_score(y_test,y_pred))

cm=confusion_matrix(y_test,y_pred)
sns.heatmap(cm,annot=True,fmt="d")
plt.title("Confusion Matrix")
plt.show()

